class StringDemo6{
         public static void main(String[] args) {
                 String s="Babulal";
                 System.out.println(s.startswith("B"));
                 System.out.println(s.startsWith("Babu"));
                 System.out.println(s.startsWith("Babulal"));
                 System.out.println(s.startsWith("babulal"));
                 System.out.println(s.startsWith("lal"));
         }
}
